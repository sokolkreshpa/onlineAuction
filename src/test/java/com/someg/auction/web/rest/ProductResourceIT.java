    @Test
    @Transactional
    void createProduct() throws Exception {
        int databaseSizeBeforeCreate = productRepository.findAll().size();
        // Create the Product
        restProductMockMvc
            .perform(post(ENTITY_API_URL).contentType(MediaType.APPLICATION_JSON).content(TestUtil.convertObjectToJsonBytes(product)))
            .andExpect(status().isCreated());

        // Validate the Product in the database
        List<Product> productList = productRepository.findAll();
        assertThat(productList).hasSize(databaseSizeBeforeCreate + 1);
        Product testProduct = productList.get(productList.size() - 1);
        assertThat(testProduct.getProductname()).isEqualTo(DEFAULT_PRODUCTNAME);
        assertThat(testProduct.getProductSpecification()).isEqualTo(DEFAULT_PRODUCT_SPECIFICATION);
        assertThat(testProduct.getActualCost()).isEqualTo(DEFAULT_ACTUAL_COST);
        assertThat(testProduct.getCcy()).isEqualTo(DEFAULT_CCY);
        assertThat(testProduct.getCreationDate()).isEqualTo(DEFAULT_CREATION_DATE);

        // Validate the Product in Elasticsearch
        verify(mockProductSearchRepository, times(1)).save(testProduct);
    }

