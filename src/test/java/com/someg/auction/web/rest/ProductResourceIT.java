    @Test
    @Transactional
    void putNewProduct() throws Exception {
        // Initialize the database
        productRepository.saveAndFlush(product);

        int databaseSizeBeforeUpdate = productRepository.findAll().size();

        // Update the product
        Product updatedProduct = productRepository.findById(product.getId()).get();
        // Disconnect from session so that the updates on updatedProduct are not directly saved in db
        em.detach(updatedProduct);
        updatedProduct
            .productname(UPDATED_PRODUCTNAME)
            .productSpecification(UPDATED_PRODUCT_SPECIFICATION)
            .actualCost(UPDATED_ACTUAL_COST)
            .ccy(UPDATED_CCY)
            .creationDate(UPDATED_CREATION_DATE);

        restProductMockMvc
            .perform(
                put(ENTITY_API_URL_ID, updatedProduct.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(updatedProduct))
            )
            .andExpect(status().isOk());

        // Validate the Product in the database
        List<Product> productList = productRepository.findAll();
        assertThat(productList).hasSize(databaseSizeBeforeUpdate);
        Product testProduct = productList.get(productList.size() - 1);
        assertThat(testProduct.getProductname()).isEqualTo(UPDATED_PRODUCTNAME);
        assertThat(testProduct.getProductSpecification()).isEqualTo(UPDATED_PRODUCT_SPECIFICATION);
        assertThat(testProduct.getActualCost()).isEqualTo(UPDATED_ACTUAL_COST);
        assertThat(testProduct.getCcy()).isEqualTo(UPDATED_CCY);
        assertThat(testProduct.getCreationDate()).isEqualTo(UPDATED_CREATION_DATE);

        // Validate the Product in Elasticsearch
        verify(mockProductSearchRepository).save(testProduct);
    }

